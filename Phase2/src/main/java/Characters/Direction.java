package Characters;
/**
 * This enum represents the possible directions that a character can move in the game.
 */
public enum Direction {
    /**
     * Represents moving up (towards the top of the screen).
     */
    UP, 

    /**
     * Represents moving down (towards the bottom of the screen).
     */
    DOWN, 

    /**
     * Represents moving left (towards the left side of the screen).
     */
    LEFT, 

    /**
     * Represents moving right (towards the right side of the screen).
     */
    RIGHT, 

    /**
     * Represents no movement.
     */
    NONE
}